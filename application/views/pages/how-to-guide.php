                    <h2>Mods &amp; Sets</h2>
                    <h4>Mods</h4>
                    <p>"Mod" is industry speak for a fragrance formula. When working on a new fragrance, a perfumer may create tens to hundreds of mods before the formula is finalized. We call each formula you create on Scent Market a "mod".
                        When you create a new mod, you can either start from scratch or work from a formula you've already made. 
                        To begin from an existing formula, click a mod in <a  href="<?php echo base_url('account/mods'); ?>">My Mods</a> and click <span class="faux-button" style="background-color:rgb(172, 203, 232)">CREATE NEW MOD +</span>.</p>
                    <p>Before getting a formula just right, perfumers usually create many versions.  Rumor has it that Chanel No. 5 was the fifth 
                        version of the fragrance that perfumer Ernest Beaux presented to Coco Chanel, and that's how the fragrance got it's name.</p>
                    <div class="divider"></div>
                    <h4>Sets</h4>
                    <p>A "set" is a group of three mods. You can select any three of your mods and create a set. The purpose of sets is to group similar mods and compare them to each other when creating your fragrances. It's easier to evaluate a mod when comparing it against other mods than to just smell it alone.
                       For example, if you are creating a floral scent, you might create a "Flower" set with three mods based on rose and jasmine.</p>
                    <img src="<?php echo base_url(); ?>/assets/public/images/products/set.jpg" width="400">
                    <p class="tc"><small><em class="nc">A Scent Market Set</em></small></p> 
                    <p>To create a new set, simply go to <a  href="<?php echo base_url('account/sets'); ?>">My Sets</a> and click <span class="faux-button" style="background-color:rgb(172, 203, 232)">CREATE NEW SET +</span> at the top of the page.
                       When you order one of your sets we create a 2 ml sample of each mod for you to evaluate.</p>
                    <div class="divider"></div>
                    <h4>Evaluation</h4>
                    <p>Here are some tips to keep in mind when you evaluate your mods.</p>
                    <p><strong>Test on skin</strong><br />
                        Don't trust paper blotters when trying your mods. Fragrance smells different on skin, and on your skin is where you'll be wearing it; so do yourself a favor and try your mods on your skin!</p>
                    <p><strong>Smell top to bottom</strong><br /> 
                        Perfumes usually evolve over time&mdash;something you love at first sniff you may dislike soon after. And vice versa, of course.</p>
                    <p><strong>Rest your nose</strong><br />
                        Noses tune out. Don't smell too many scents per hour or per day, to keep your nose reliable.</p>
                    <p><strong>Check the radiance</strong><br />
                        Curious how your perfume will smell to other people as you walk past? Spray a blotter and leave it in the 
                        room while you step out for a bit. Come back fifteen minutes later and breathe in: that's the radiance.</p>
