                    <h4>How To Use</h4>
                    <p>Anyone can improve their sense of smell by spending time getting to know different raw materials.</p> 
                    <p><strong>1/</strong> Smell individual raw materials often on blotters and observe how each scent evolves over
                    the course of a day. Top notes will move from strong to faint, while base notes soften and settle in over time.</p>
                    <p><strong>2/</strong> Blind tests are a great way to memorize different materials. Dip blotters in different raw materials, label them, then try to distinguish and identify them from memory.</p>
                    <p><strong>3/</strong> Dip multiple blotters in different raw materials and smell the blotters together like a fan to get an idea of how the materials smell in combination. Add and remove different raw materials to discover the effect each one has on the scent.</p>
