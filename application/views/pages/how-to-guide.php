                    <h2>Glossary</h2>
                    <img src="<?php echo base_url(); ?>assets/public/images/photos/typewriter-apart.jpg" />
                    <p>When people withing the fragrance industry describe a scent it can sound like a foreign language: woody, aldehydic, powdery, balsamaic...But like anything else, it's important for professionals and non-professionals to be able to communicate. On that note, here are some common fragrance terms that result in blank stares or empty nods more often than not.</p>
                    <h4>Absolute</h4>
                    <p>Absolutes are essentially highly concentrated essential oils.</p>
                    <h4>Animalic</h4>
                    <p>Previously attributed to scents with animal-derived origins, most animalic ingredients today are aromachemicals. When smelled alone, animalic notes can be intense and unpleasant, but used in small amounts they add sensuality and depth to a fragrance.</p>
                    <h4>Aldehydic</h4>
                    <p>Aldehydes are synthetic materials popularized by the heavy use of them in Chanel No. 5. They are the same organic compounds as ketones and though their scents vary, they tend to add lift, air, and "sparkle" to a fragrance.</p>
                    <h4>Accord</h4>
                    <p>An accord is a combination of ingredients that together create the effect of another material. For example, you may see a ‘candied-cherry’ accord. A candied cherry cannot be bottled up and put in a fragrance, instead a perfumer will find a combination of ingredients that come together to create a ‘candied-cherry’ scent or effect.</p>
                    <h4>Aromatic</h4>
                    <p>This term refers to raw materials and notes that are vibrant, energetic, fresh. Aromatic notes are common in masculine fragrances.</p>
                    <h4>Balsamic</h4>
                    <p>Balsamic refers to a family of raw materials that are sweet, soft and warm. Most are derived from resins and gums and they're important in Oriental fragrances.</p>
                    <h4>Chypre</h4>
                    <p>Chypre (pronounced "sheepra") refers to a family of fragrances originating from the mossy, woody, citrusy perfume Chypre (Coty, 1917). Classic chypre fragrances generally had sparkling citrus and floral notes over a dark, earthy base of oakmoss, patchouli, woods and labdanum. Modern chypre fragrances use less (or zero) oakmoss due to regulatory restrictions; sometimes they use synthetic substitutes. </p>
                    <h4>Concentration</h4>
                    <p>Perfume is made up of scented oils and alcohol. The ratio of oil to alcohol is the "concentration" and it is typically between 10 to 20%. Parfum is the most concentrated form of fragrance and eau de cologne the least.</p>
                    <h4>Essential Oil</h4>
                    <p>An essential oil is a natural oil that carries the distinctive scent, or essence, of the plant (flowers, leaves, wood, bark, roots, seeds, or peel) from which it is derived.</p>
                    <h4>Flanker</h4>
                    <p>Flankers are like TV spinoffs, but if the original was a perfume and the spinoff was a twist or interpretation of that perfume. Flankers are ever popular today and some are less obvious than others. Bestseller Coco Mademoiselle, for example, is a flanker of the original Coco. Many flankers are released as limited editions. There have been nearly 20 flankers of L'Eau D'Issey Pour Homme released since the original was created in 1994. Usually, flankers have the same bottle as the original fragrance, but in different colors or with a different decoration.</p>
                    <h4>Fougère</h4>
                    <p>Fougère is a fragrance family (the word means 'fern' in French) originating from Fougère Royale (Houbigant, 1884). The fougère structure includes notes like lavender, coumarin, oakmoss, woods, and bergamot.</p>
                    <h4>Mod</h4>
                    <p>"Mod" is industry speak for a fragrance formula. We call each formula you create on Scent Market a "mod".</p>
                    <h4>Musk</h4>
                    <p>The name musk originates from an odorous substance obtained from a gland of the male musk deer that was historically used in perfumery. The name now encompasses a family of scents with similar odors. The musk odors used today are all aromachemicals.</p>
                    <h4>Note</h4>
                    <p>Notes are the raw materials or scents that make up a fragrance. A ‘note’ can be a single ingredient (natural or synthetic), or an accord.</p>
                    <h4>Palette</h4>
                    <p>A box of small samples of blends and raw materials. <a  href="<?php echo base_url('palettes'); ?>">Order a palette</a> to smell materials before or while creating your mods.</p>
                    <h4>Powdery</h4>
                    <p>Often, fragrance descriptions move beyond simple smells and refer to the perceived texture or aesthetic of the scent. The scent of jam is sticky, the scent of butter is creamy. A scent, such as iris, that is perceived as soft and somewhat dry is referred to as "powdery."</p>
                    <h4>Set</h4>
                    <p>A "set" is a group of three mods. You can select any three of your mods and create a set. The best way to evaluate your mods is to smell them against other mods.</p>
