                    <h2>Raw Materials</h2>
                        <p>Raw materials are the individual ingredients that make up a perfume formula. There are
                            thousands of different raw materials sourced from all over the world. Rose, for example, comes
                            in hundreds of different forms depending on the type of rose, where it is grown, and how it is
                            extracted.</p>
                        <img src="<?php echo base_url('assets/public/images/how-to-guide/raw-material-directory.jpg') ?>" alt="scent market raw materials directory" style="border:1px solid #eee; margin-bottom:20px; width:700px;" />
                        <p>To get to know different raw materials, browse them in the <a href="<?php echo base_url('raw-materials'); ?>">directory</a> or while in the lab. 
                            Information is provided for each that will be very helpful while creating your fragrances. Here's what it all means!</p>
                        <h4 class="rm-header">1/ Scientific Name</h4>
                        <div>
                            <p>If the raw material is natural, the scientific name is shown.</p>
                        </div>
                        <h4 class="rm-header">2/ Family</h4>
                        <div>
                            <p>Raw materials are organized into families that contain materials with simmilar scents. 
                                In the <a href="<?php echo base_url('raw-materials'); ?>">directory</a> you can filter raw materials by family to learn more about the scent and uses for the raw materials in each family. 
                        </div>
                        <h4 class="rm-header">3/ Scent</h4>
                        <p>Each raw material includes a short description of its scent. These words can describe many facets of scent including 
                            texture and strength. Like any language it may take time to fully understand the language of perfume. Phrases like powdery or 
                            balsamic make more sense as you become familiar with different types of smells over time.</p>
                        <h4 class="rm-header">4/ Note</h4>
                        <div>
                            <p>Raw materials are classified as top notes, heart notes, or base notes according to their volatility, or how quickly their scent evaporates. Most fragrances are built with a combination of top, heart, and base notes to balance a fragrance as it evolves over time. A fragrance without base notes will be shortlived, and one without top notes will lack a strong impression upon first spritz. Heart notes round out a perfume by blending with the top notes and softening the harsher facets of the base notes.</p> 
                            <p><strong>&#9650; Top Notes</strong><br />
                            Oils with lighter molecules that evaporate the fastest, so when smelling a fragrance you smell them first.
                            They're the first impression of a fragrance, but they don't linger for long giving way to the heart and base notes.
                            Citrus raw materials are common top notes.</p> 
                            <p><strong>&hearts; Heart Notes</strong><br />
                            Heart notes round out a perfume by blending with the top notes and softening the harsher facets of the base notes. Most floral raw materials are heart notes, as well as many spicy raw materials.</p> 
                            <p><strong> &#9660;Base Notes</strong><br /> 
                            Evaporate the slowest, have the longest staying power and thus determine much of the character of a fragrance. Though top notes are responsible for the first impression, base notes last for hours on end. Woody, sweet, and balsamic raw materials are usually base notes.</p>
                        </div>
                        <h4 class="rm-header">5/ Type</h4>
                        <div>
                            <p>There are a lot of misconceptions about the ingredients used in fragrance. Natural ingredients are viewed as "good" while synthetic ingredients are viewed as "bad". 
                                In reality, synthetic ingredients make up to 80% of many modern perfumes and have been used prominently since the start of modern perfumery at the the turn of the 19th century.</p> 
                            <div class="directory-key">
                                <div>
                                    <h2>Naturals</h2>
                                    <p class="rm-badge rm-badge-eo">Eo</p>
                                    <p><b>Essential Oil.</b> Extracted from natural materials through steam distillation or cold press resulting in an oil or essence.</p>

                                    <p class="rm-badge rm-badge-ab">Ab</p>
                                    <p><b>Absolute.</b> Obtained from natural materials through alcohol extraction. The residual product that contains perfume content after extraction is the concrete. When the concrete is further filtered, the product obtained after evaporating all of the alcohol is the absolute. </p>

                                    <p class="rm-badge rm-badge-co2">CO<sub>2</sub></p>
                                    <p><b>CO<sub>2</sub>.</b> Supercritical CO2 extraction is a recent invention that uses carbon dioxide in its liquid state to produce an absolute that can retain the original odor of the natural material.</p>
                                </div>
                                <div>
                                    <h2>Synthetics</h2>
                                    <p class="rm-badge rm-badge-ac">Am</p>
                                    <p><b>Aroma Molecule.</b> Molecules that are synthesized in a lab. Some have unique scents that do not occur in nature, and others resemble natural odors that are not available as natural extracts for various technical and economic reasons.</p>

                                    <p class="rm-badge rm-badge-bl">Bl</p>
                                    <p><b>Blend.</b> A combination of natural and synthetic materials that resemble a specific scent. Also called "accords."</p>
                                </div>
                            </div><!--.directory-key-->
                            <p>Synthetic materials allow perfumers to expand their palettes, reinvent naturally occurring smells, and create entirely new smells. 
                                They are arguably less dangerous as allergens than natural materials because whereas rose oil may contain some hundreds of different molecules, a synthetic ingredient is composed of only one. 
                                Some synthetic molecules occur naturally in nature, and some are novel. For example, the molecule Citronellol occurs naturally in geranium, rose, and lemongrass, 
                                but it is also used on its own to add a floral-rosy, dusty, pepper, citronella note to a fragrance. 
                                In contrast, the molecule Lyral does not occur in nature and is used to add a muguet, green, aldehydic, fresh note.</p>
                            <p>Natural raw materials are derived from flowers, wood, fruit, and the like, but obviously your perfumes
                                don't contain wood chips and flower petals. Various methods are used to capture
                                natural scent and each method returns different results. This is why raw materials come in a number of types.</p>
                            <p>Many natural substances produce a variety of raw materials depending on the extraction method used and the part of the substance
                                that is extracted. For example, the bitter orange tree produces three common raw materials: <a href="<?php echo base_url('lab/petit-grain'); ?>" >petit grain</a> (steam distilled from
                                branches and leaves), 
                                <a href="<?php echo base_url('lab/neroli'); ?>" >neroli oil</a> 
                                (steam distilled from the flower), and 
                                <a href="<?php echo base_url('lab/orange-flower-absolute'); ?>" >orange flower absolute</a>
                                (extracted with alcohol from the flower). Rose absolute, obtained through alcohol extraction, and rose oil, obtained through steam distillation,
                                of the same rose plant also produce materials with different scents.</p>
                        </div>
