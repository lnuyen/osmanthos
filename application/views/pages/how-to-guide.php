                    <h2>Create A Fragrance</h2>
                    <h4 style="margin-top:40px;">Fragrance, Perfume &amp; Scent</h4>
                    <p>Many people think perfumery is as simple as identifying smells you like and 
                        combining them in a bottle. If only it were so easy. Professional
                        perfumers train for many years to memorize thousands of smells, learn how
                        different ingredients interact with each other, and find the perfect combination of materials to take a
                        fragrance from an idea to reality.</p>
                    <p>People come to Scent Market with different goals&mdash;to create a blend of your favorite raw materials,
                        to make something truly unique, or perhaps to experiment in the lab. 
                        Whatever your end goal, we guide you through the process of creating your own fragrance to help you create the best fragrance for 
                        you.</p>
                    <div class="divider"></div>
                    <h4>1/ Identify What You Like</h4>
                    <p>An important step before you start creating a fragrance is to identify scents and styles that you
                        like. It helps to take a look at your favorite fragrances and try to pinpoint
                        why you love them &mdash; the way it makes you feel, a particular ingredient,
                        or perhaps the total package.</p>
                    <p>Like raw materials, fragrances are organized into families. There are
                        different classification systems &mdash; one popular system is
                        the <a href="http://www.fragrancesoftheworld.com/external/wheel/index.html" target="_blank">fragrance wheel</a>
                        created by fragrance expert Michael Edwards. The fragrance wheel contains
                        fourteen different families that are grouped next to similar families. If you find yourself
                        drawn to fragrances in one family, you may like fragrances in the neighboring families as
                        well. If it's not obvious where your favorite fragrances fall on the fragrance wheel, search for them in our <a href="fragrances">
                        directory</a> or at <a href="http://osmoz.com">osmoz.com</a> to find out the main
                        raw materials and family.</p>
                        <div class="divider"></div>
                    <h4>2/ Start Simple</h4>
                    <p>Most people know if they like jasmine, rose, or lemon &mdash; many do not know if they like lesser known ingredients like galbanum or hedione. 
                        Start by building a formula with ingredients you're familiar with, then experiment by adding new materials, or order a <a href="palettes">palette</a> to get to know new ingredients.
                    </p>
                        <div class="divider"></div>
                    <h4>3/ Build Character &amp; Add Dimension</h4>
                    <p>Once you have a starting point &mdash; ie. one of our blends blend, a floral bouquet, an amber base &mdash; begin to add character.</p>
                    <p style="padding-left:20px;">
                        <strong>Increase longevity</strong><br/>
                        Base notes evaporate the slowest, have the longest staying power and thus determine much of the character of a fragrance.<br/>
                        amber notes, woody notes, sweet notes, musk notes, leather<br/><br/>
                        <strong>Add character</strong><br/>
                        Heart notes follow the top notes and add character to the base notes.<br>
                        floral notes, fruity notes, spicy notes, green notes, aromatic/herbal notes<br/><br/>
                        <strong>Create a first impression</strong><br/>
                        Top notes are made up of lighter molecules that evaporate the fastest, so when smelling a fragrance you smell them first.<br>
                        citrus notes, aromatic/herbal notes, green notes, spicy notes<br/><br/>
                        <strong>Improve diffusion</strong><br/>
                        Diffusion refers to a fragrance's ability to spread and disperse through the air. Certain ingredients seemingly add lightness and airiness to a fragrance, helping to improve diffusion.<br>
                        hedione, iso e super<br/><br/>
                        <strong>Smooth rough edges</strong><br/>
                        Some fragrances feel a bit raw or rough around the edges. Certain ingredients are excellent at smoothing those rough edges and blending a fragrance together.<br>
                        vanilla, musk, benzoin
                    </p>
                        <div class="divider"></div>
                    <h4>4/ Experiment</h4>
                    <p>Create multiple mods from similar formula. To make modifications to an existing formula, click a mod in <a  href="<?php echo base_url('account/mods'); ?>">My Mods</a>, then click <span class="faux-button">CREATE NEW MOD +</span>.</p>
